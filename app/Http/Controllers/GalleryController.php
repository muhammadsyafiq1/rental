     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->all();
        $data['foto'] = $request->file('foto')->store('gallery_mobil','public');
        Gallery::create($data);
        return redirect()->back()->with('status','Gallery mobil berhasil Ditambahkan.');
    }

